when it was known as the Spanish Prisoner scam. People were asked to
help secure the release from prison of &nbsp;the son of a wealthy
Spanish family, who would -of course- be very financially grateful. But
the result then was the same as now, the victims ended up out of
pocket. The modern day problem has gotten so bad, that in the US the
